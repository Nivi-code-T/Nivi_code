#1D Find Standard Deviation of array Elements in Numpy
import numpy as np
n1=np.array([1,2,3,4,5])
n2=np.array([1,2,3,4,5])
print("Standard Deviation of Array1",np.std(n1))
print("Standard Deviation of Array2",np.std(n2))

